public class IdenticalMembersException extends Exception{

    public IdenticalMembersException() { super(); }
    public IdenticalMembersException(String message) { super(message); }
    public IdenticalMembersException(String message, Throwable cause) { super(message, cause); }
    public IdenticalMembersException(Throwable cause) { super(cause); }
    
}
    
